/* jshint jasmine:true */
/* globals StyledElements */

(function () {

    "use strict";

    describe("Styled PasswordField", function () {
        var dom = null;

        beforeEach(function () {
            dom = document.createElement('div');
            document.body.appendChild(dom);
        });

        afterEach(function () {
            if (dom != null) {
                dom.remove();
                dom = null;
            }
        });

        it("should create an instance with no options", function () {
            var element = new StyledElements.PasswordField();

            expect(element.wrapperElement.className).toEqual("se-password-field");
        });
    });

})();
